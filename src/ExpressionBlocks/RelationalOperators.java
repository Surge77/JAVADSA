package ExpressionBlocks;

public class RelationalOperators {

    public static void main(String[] args) {

        //Relational Operators are used to check the relation between two operands

        //Relational operators will return true or false that means either they will be compared or not compared

        int a = 8, b = 20;

        System.out.println(a + " " + b);

        // == Operator (is Equal to )

        System.out.println(a == b); //will return either true or false

        System.out.println(a != b); // not equal to

        //similarly other relational operators
        System.out.println(a < b);
        System.out.println(a > b);
        System.out.println(a >= b);
        System.out.println(a <= b);

        //Relational operators are used in decision making process and loops

    }
}
