package ExpressionBlocks;

public class ArithemticOperators {

    public static void main(String[] args) {

        //Arithmetic operators are used to perform arithmetic operations on variables and data

        int a = 12, b = 5; // we can declare two variables in same line of same data type

        System.out.println(a - b);
        System.out.println(a + b);
        System.out.println(a * b);
        System.out.println(a / b); //prints quotient
        System.out.println(a % b); //prints remainder

    }
}
