package ExpressionBlocks;

public class LogicalOperators {

    public static void main(String[] args) {

        // Logical Operators are used to check whether an expression is true or false
        //They are used in decision making

        // && => (Logical AND) Expression1 and Expression2
        // True if only both epression 1 and 2 are true

        System.out.println((5 > 3)&&(6>4)); //Be sure to add double brackets
        System.out.println((5 < 3)&&(6>4));

        // || (Logical OR) Exprn1 || express2
        // True if either one of them is true

        // ! (Logical NOT) !expression
        //True is false and vice versa
    }
}
